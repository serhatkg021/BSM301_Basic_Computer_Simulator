    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        jFileChooser1.showOpenDialog(this);
        File f = jFileChooser1.getSelectedFile();
        try {
            BufferedReader reader = new BufferedReader(new FileReader(f));
            String line = null;
            while (reader.ready()) {
                line = reader.readLine();
                if (line.charAt(0) != '\u0000' && line.charAt(0) != '/') {
                    taAssamblyCode.append(line + "\n");
                    String[] lineSplit = line.split(" ");
                    if (lineSplit[0].contains("ORG")) {
                        int temp = Integer.parseInt(lineSplit[1]);
                        _Grup_27_Command.PC = Integer.toBinaryString(temp);
                        int temp2 = _Grup_27_Command.PC.length();
                        for (int i = 0; i < 4 - temp2; i++) {
                            _Grup_27_Command.PC = "0" + _Grup_27_Command.PC;
                        }
                        lblPC.setText(_Grup_27_Command.PC);
                        lblPC.setBackground(Color.pink);
                    }
                    code.add(lineSplit);
                }
            }
            reader.close();
        } catch (Exception ex) {
            System.err.println(ex);
        }

        int startAdress = Integer.parseInt(code.get(0)[1]);
        String lineOpCode = null;
        for (int i = 1; i < code.size() - 1; i++) {
            String addAdress = mtd.getAdressFormat(String.valueOf(startAdress));
            String[] lineCode = code.get(i);
            if (lineCode[0].charAt(lineCode[0].length() - 1) == ',') {
                if (lineCode[1].charAt(0) == 'D') {
                    lineOpCode = mtd.convertDecToHex(lineCode[2]);
                } else {
                    lineOpCode = lineCode[2];
                }
            } else {
                lineOpCode = lineCode[0];
            }
            String[] symbolCodeLine = {addAdress + startAdress, lineOpCode, ""};

            symbolCode.add(symbolCodeLine);
            startAdress++;
        }

        for (int i = 1; i < code.size(); i++) {
            if (code.get(i)[0].charAt(code.get(i)[0].length() - 1) == ',') {
                taLabelTable.append(code.get(i)[0] + " " + code.get(i)[2] + "\n");
                for (int j = 1; j < code.size(); j++) {
                    if (code.get(j)[0].charAt(code.get(j)[0].length() - 1) != ',') {
                        if (code.get(j).length > 1) {
                            if (code.get(i)[0].charAt(0) == code.get(j)[1].charAt(0)) {
                                symbolCode.get(j - 1)[2] = symbolCode.get(i - 1)[0];
                            }
                        }
                    }
                }
            }
        }

        String hexConvert = null;
        for (int i = 0; i < symbolCode.size(); i++) {
            String[] lineCode = code.get(i + 1);
            if (lineCode[0].charAt(lineCode[0].length() - 1) == ',') {

                hexConvert = symbolCode.get(i)[1];
            } else {
                hexConvert = commands.getHexCodeValue(symbolCode.get(i)[1]) + symbolCode.get(i)[2];
            }
            String[] hexCodeLine = {symbolCode.get(i)[0], hexConvert, ""};
            hexCode.add(hexCodeLine);

            for (int j = 0; j < symbolCode.get(i).length; j++) {
                taSymbolCode.append(symbolCode.get(i)[j] + "   ");
            }
            taSymbolCode.append("\n");
        }

        String binaryConvert = null;
        for (int i = 0; i < hexCode.size(); i++) {
            binaryConvert = mtd.hexToBinary(hexCode.get(i)[1]);
            String[] binaryCodeLine = {mtd.hexToBinaryAdress(hexCode.get(i)[0]), binaryConvert, ""};
            binaryCode.add(binaryCodeLine);

            for (int j = 0; j < hexCode.get(i).length; j++) {
                taHexCode.append(hexCode.get(i)[j] + " ");
            }
            taHexCode.append("\n");
        }

        for (int i = 0; i < binaryCode.size(); i++) {
            for (int j = 0; j < binaryCode.get(i).length; j++) {
                taBinaryCode.append(binaryCode.get(i)[j] + "    ");
            }
            taBinaryCode.append("\n");
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void btnDerleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDerleActionPerformed
        // TODO add your handling code here:
        boolean hlt = false;
        int row = Integer.parseInt(_Grup_27_Command.PC, 2);
        for (int i = 0; i < symbolCode.size(); i++) {
            String[] memoryRow = new String[3];
            if (hlt == false) {
                String[] symbolRow = _Grup_27_FindSymbol.findSymbol(symbolCode.get(i)[1], symbolCode.get(i)[2]);
                memoryRow[0] = symbolRow[0];
                memoryRow[1] = symbolRow[1];
                memoryRow[2] = symbolRow[2];
                if (symbolCode.get(i)[1].contains("HLT")) {
                    hlt = true;
                }
            } else {
                memoryRow[0] = "0";
                memoryRow[1] = "0000";
                memoryRow[2] = symbolCode.get(i)[1];
            }

            tblMemory.setValueAt(memoryRow[0], row, 1);
            tblMemory.setValueAt(memoryRow[1], row, 2);
            tblMemory.setValueAt(memoryRow[2], row, 3);
            row++;
        }
    }//GEN-LAST:event_btnDerleActionPerformed
