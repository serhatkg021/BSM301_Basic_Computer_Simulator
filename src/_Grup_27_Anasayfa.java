        String mif = "";
        for (int i = 0; i < hexCode.size(); i++) {
            mif = mif + hexCode.get(i)[1] + "\n";
        }
        File file = new File("dosya.mif");
        if (!file.exists()) {
            try {
                file.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(_Grup_27_Anasayfa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        FileWriter fileWriter = null;
        try {
            fileWriter = new FileWriter(file, false);
        } catch (IOException ex) {
            Logger.getLogger(_Grup_27_Anasayfa.class.getName()).log(Level.SEVERE, null, ex);
        }
        BufferedWriter bWriter = new BufferedWriter(fileWriter);
        try {
            bWriter.write(mif);
        } catch (IOException ex) {
            Logger.getLogger(_Grup_27_Anasayfa.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            bWriter.close();
        } catch (IOException ex) {
            Logger.getLogger(_Grup_27_Anasayfa.class.getName()).log(Level.SEVERE, null, ex);
        }
